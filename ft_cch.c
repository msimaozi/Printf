#include "ft_printf.h"

int	ft_c(int ap)
{
	int	x;

	x = 0;
	if (ap <= 0)
		x++;
	while (ap != 0)
	{
		ap = ap / 10;
		x++;
	}
	return (x);
}

int	ft_c_hexa(uintptr_t ap)
{
	size_t	x;

	x = 0;
	while (ap != 0)
	{
		x++;
		ap = ap / 16;
	}
	return (x);
}
